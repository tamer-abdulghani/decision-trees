    public void displayTrainingCharasCategoricalOnly(DataSet model) {
        String result = "Loaded Data \n";
        for (Row r : model.getRows()) {
            result += r.getRowId() + ":";
            for (Map.Entry<Characteristic, Value> el : r.getValuesMap().entrySet()) {
                result += el.getValue() + ",";
            }
            result += "\n";
        }
        jTextArea5.setText(result);

        ArrayList<Characteristic> list = model.getCharas();
        Object[] array = list
                .stream()
                .filter(x -> x.getName().toLowerCase().equals("sex")
                || x.getName().toLowerCase().equals("pclass")
                || x.getName().toLowerCase().equals("sibsp")
                || x.getName().toLowerCase().equals("parch")
                )
                .collect(Collectors.toList())
                .toArray();

        this.jComboBox1.setModel(new DefaultComboBoxModel(array));
    }

